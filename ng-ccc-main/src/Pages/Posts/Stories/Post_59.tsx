        <p>古代絲綢之路並不是一條安全的路線（某些途徑點至今處於動盪），這給絲綢之路的發展造成了巨大的阻礙。比起具有一定規模和組織性的戰爭事件，地理環境的嚴酷、強盜之亂等只是微不足道的因素。加之十五世紀開始海上貿易的盛行，以及同時期明朝的封閉，加劇了這條財富之路的衰落。不過，至今，絲綢之路在文化上的影響力依然是巨大的。文化是通過不斷的交流而被促進的，而有關絲綢之路的回憶，也時刻提醒著我們過去的繁榮從何而來——它不僅是商業上的，也是文化上的一種多元繁榮的標誌。</p>
